/** Method to edit the data from the userdetail.txt file 
 */
    private void submitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_submitActionPerformed
        // TODO add your handling code here:
        Main t = new Main();
        this.row[t.s][1] = this.ip.getText();
        this.row[t.s][5] = this.loc.getText();

        Writer wr;

        try {
            wr = new BufferedWriter(new FileWriter("recources/database.txt", false));
            for (int i = 0; i < this.llines; i++) {
                String r = "";
                for (int j = 0; j < 7; j++) {
                    r += row[i][j] + ",";

                }
                r += "\n";
                wr.append(r);
            }
            wr.close();
        } catch (IOException ex) {
            Logger.getLogger(Update.class.getName()).log(Level.SEVERE, null, ex);
        }



    }//GEN-LAST:event_submitActionPerformed

